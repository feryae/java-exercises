public class Labour extends Employee{
    private int day;
    public double getSalary(){
        return getDay() *   super.getSalary();
    }

    /**
     * @return the day
     */
    public int getDay() {
        return day;
    }

    /**
     * @param day the day to set
     */
    public void setDay(int day) {
        this.day = day;
    }
}
